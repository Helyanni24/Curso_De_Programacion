let diaSemana = 9;

switch (diaSemana){
  case 1:
    console.log("Lunes: lentejas");
    break;
  case 2:
    console.log("Martes: pollo al horno");
    break;
  case 3:
    console.log("Miércoles: pescado a la plancha");
    break;
  case 4:
    console.log("Jueves: pasta");
    break;
  case 5:
    console.log("Viernes: paella");
    break;
  default:
    console.log("Día no valido para menu.");}
